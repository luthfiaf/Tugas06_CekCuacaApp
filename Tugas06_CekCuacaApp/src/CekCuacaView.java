    private void saveKotaFavoritToFile() {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter("kotaFavorit.txt"))) {
        for (String kota : kotaFavorit) {
            writer.write(kota);
            writer.newLine();
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}
    
    private void loadKotaFavoritFromFile() {
    kotaFavorit.clear();  // Clear existing data
    try (BufferedReader reader = new BufferedReader(new FileReader("kotaFavorit.txt"))) {
        String line;
        while ((line = reader.readLine()) != null) {
            kotaFavorit.add(line);
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    updateKotaFavoritComboBox();  // Update ComboBox after loading
}
    
    // Fungsi untuk mengambil data cuaca dari API
    private String getWeatherData(String kota) {
       try {
        String urlString = "https://api.openweathermap.org/data/2.5/weather?q=" + kota + "&appid=" + apiKey;
        URL url = new URL(urlString);
        HttpURLConnection conn = (HttpURLConnection) url.openConnection();
        conn.setRequestMethod("GET");

        Scanner scanner = new Scanner(url.openStream());
        String response = scanner.useDelimiter("\\A").next();
        scanner.close();

        JSONObject jsonObject = new JSONObject(response);
        String kondisiCuaca = jsonObject.getJSONArray("weather").getJSONObject(0).getString("description");
        String iconCode = jsonObject.getJSONArray("weather").getJSONObject(0).getString("icon"); // Ambil kode icon
        double suhu = jsonObject.getJSONObject("main").getDouble("temp") - 273.15; // Konversi dari Kelvin ke Celsius

        String iconUrl = "http://openweathermap.org/img/wn/" + iconCode + "@2x.png";
        URL urls = new URL(iconUrl);
        ImageIcon originalIcon = new ImageIcon(ImageIO.read(urls));
        // Tentukan ukuran maksimum untuk ikon
        int maxWidth = 50; // Ganti dengan lebar maksimum yang diinginkan
        int maxHeight = 50; // Ganti dengan tinggi maksimum yang diinginkan
        // Ubah ukuran ikon
        Image img = originalIcon.getImage();
        Image scaledImg = img.getScaledInstance(maxWidth, maxHeight, Image.SCALE_SMOOTH);
        jLabelGambarCuaca.setIcon(new ImageIcon(scaledImg)); // Set ikon cuaca
        
        // Kembalikan string hasil cuaca dan kode icon
        return "Kota: " + kota + "\nCuaca: " + kondisiCuaca + "\nSuhu: " + String.format("%.2f", suhu) + "°C" + "\nIcon: " + iconCode;
    } catch (Exception ex) {
        ex.printStackTrace();
        return "Error: Gagal mendapatkan data cuaca.";
    }
    }

