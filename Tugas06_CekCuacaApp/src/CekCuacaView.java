    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
      // Membuka JFileChooser untuk memilih lokasi dan nama file
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Pilih Lokasi untuk Menyimpan File");
    fileChooser.setSelectedFile(new java.io.File("dataCuaca.csv")); // Menetapkan nama file default

    // Memunculkan dialog Simpan
    int result = fileChooser.showSaveDialog(this);
    if (result == JFileChooser.APPROVE_OPTION) {
        // Mendapatkan file yang dipilih pengguna
        java.io.File file = fileChooser.getSelectedFile();
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            // Tulis header CSV
            writer.write("Kota,Cuaca,Suhu");
            writer.newLine();

            // Ambil data dari tabel dan tulis ke dalam file CSV
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                String kota = (String) model.getValueAt(i, 0);
                String cuaca = (String) model.getValueAt(i, 1);
                String suhu = (String) model.getValueAt(i, 2);

                // Tulis baris data
                writer.write(kota + "," + cuaca + "," + suhu);
                writer.newLine();
            }

            JOptionPane.showMessageDialog(this, "Data cuaca berhasil disimpan ke CSV.");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data ke CSV.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Penyimpanan dibatalkan.");
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

