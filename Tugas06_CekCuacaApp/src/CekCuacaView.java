    private void btnFavoritActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFavoritActionPerformed
        // TODO add your handling code here:
        String kota = jTextFieldKota.getText().trim();
    if (kota.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama kota terlebih dahulu.");
        return;
    }

    String hasilCuaca = getWeatherData(kota);
    jTextArea1.setText(hasilCuaca);

    // Menambahkan data cuaca ke tabel
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.addRow(new Object[]{kota, hasilCuaca.split("\n")[1], hasilCuaca.split("\n")[2]});

    // Ambil kode icon dari hasil cuaca
    String iconCode = hasilCuaca.split("\n")[3].split(": ")[1]; // Ambil kode icon
    String imagePath = "https://openweathermap.org/img/wn/" + iconCode + "@2x.png"; // URL gambar

    // Menghapus JLabel gambar cuaca sebelumnya jika ada
    if (jPanel1.getComponentCount() > 0) {
        jPanel1.remove(0); // Menghapus komponen pertama (JLabel gambar)
    }

    // Menampilkan gambar cuaca di JLabel dalam thread terpisah
    new Thread(() -> {
        try {
            JLabel weatherImageLabel = new JLabel(new ImageIcon(new URL(imagePath))); // Mengambil gambar dari URL
            jPanel1.add(weatherImageLabel); // Menambahkan JLabel ke panel utama
            jPanel1.revalidate();
            jPanel1.repaint();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal mengambil gambar cuaca.");
        }
    }).start();
        
    }//GEN-LAST:event_btnFavoritActionPerformed

